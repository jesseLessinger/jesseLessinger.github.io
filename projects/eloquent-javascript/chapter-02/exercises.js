////////////////////////////////////////////////////////////////////////////////

function fizzBuzz(start, end) {
  for(let i = start; i < end + 1; i++) {
    
    let str = "";
    if (i % 3 === 0) {
      str += "fizz";
    } 
    if (i % 5 === 0) {
      str += "buzz";
    }
    if (str) {
      console.log(str);
    } else {
      console.log(i);
    }
    
  }
}

////////////////////////////////////////////////////////////////////////////////
// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(size) {
  
  let board = "";  
  
  for (let i = 0; i < size; i++) {
    for (let j = 0; j < size; j++) {
      board += (i % 2 === j % 2) ? " " : "#"; 
    }
    board += "\n";
  }

  console.log(board);
}

